import SwiftUI

extension Color {
    static let theme = ColorTheme()
}

struct ColorTheme {
    let accent = Color(.accent)
    let background = Color(.background)
    let green = Color(.green)
    let red = Color(.red)
    let secondaryText = Color(.secondaryText)
}
